print('ㄱ) sort_items_kf:', sort_items_kf)

# ㄴ) 
def dictvalue(item):
    return -item[1] # by value from (key, value)

sort_items_vf = sorted(kimchi_dict.items(), key=dictvalue) # by value from (key, value)
print('ㄴ) sort_items_vf:', sort_items_vf)

# ㄷ) 
sort_items_kl = sorted(kimchi_dict.items(), key=(lambda item:item[0])) # by key from (key, value)
print('ㄷ) sort_items_kl:', sort_items_kl)

# ㄹ) 
sort_items_vl = sorted(kimchi_dict.items(), key=(lambda item:-item[1]))# by value from (key, value)
print('ㄹ) sort_items_vl:', sort_items_vl)


